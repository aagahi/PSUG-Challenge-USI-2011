case class Game( id : String = "", questions:Seq[Question], timeoutSec:Int=0, numPlayer:Int=0 ) extends Data[String]{
  def storeKey:String = id
  def copyWithAutoGeneratedId( id:Int ) = Game( id.toString, questions, timeoutSec, numPlayer )
}

case class Question( question:String, answers:Seq[Answer])

case class Answer( anwser:String, status:Boolean )


object InMemoryGameRepository extends InMemoryDataRepository[String,Game]



