package org.psug.usi.domain

import org.psug.usi.store.{Data, InMemoryDataRepository}

object User{
    def apply(firstName : String, lastName : String, email : String, password : String):User = User( 0, firstName, lastName, email, password )
}
case class User( id : Int, firstName : String, lastName : String, email : String, password : String) extends Data[String]{
  def storeKey:String = email
  def copyWithAutoGeneratedId( id:Int ) = User( id, firstName, lastName, email, password )
}

object InMemoryUserRepository extends InMemoryDataRepository[String,User]


